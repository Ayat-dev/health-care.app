-- ── radiology_requests ─────────────────────────────────────────────────────────
CREATE TABLE radiology_requests (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    request_number  VARCHAR(25)  NOT NULL UNIQUE,
    consultation_id BIGINT REFERENCES consultations (id),
    patient_id      BIGINT NOT NULL REFERENCES patients (id),
    doctor_id       BIGINT NOT NULL REFERENCES users (id),
    requested_at    TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    priority        VARCHAR(10) NOT NULL DEFAULT 'NORMAL',     -- NORMAL, URGENT
    status          VARCHAR(20) NOT NULL DEFAULT 'EN_ATTENTE', -- EN_ATTENTE, EN_COURS, VALIDE, LIVRE, ANNULE
    clinical_info   TEXT,
    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_radiology_requests_patient ON radiology_requests (patient_id);
CREATE INDEX idx_radiology_requests_doctor  ON radiology_requests (doctor_id);
CREATE INDEX idx_radiology_requests_status  ON radiology_requests (status);

-- ── radiology_request_items ──────────────────────────────────────────────────────
CREATE TABLE radiology_request_items (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    radiology_request_id BIGINT NOT NULL REFERENCES radiology_requests (id) ON DELETE CASCADE,
    exam_id              BIGINT NOT NULL REFERENCES radiology_exam_catalog (id)
);

CREATE INDEX idx_radiology_request_items_request ON radiology_request_items (radiology_request_id);

-- ── radiology_reports ──────────────────────────────────────────────────────────────
CREATE TABLE radiology_reports (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    radiology_request_id BIGINT NOT NULL UNIQUE REFERENCES radiology_requests (id),
    radiologist_id       BIGINT REFERENCES users (id),
    findings             TEXT,
    conclusion           TEXT,
    validated_at         TIMESTAMP,
    validated_by         BIGINT REFERENCES users (id),
    created_at           TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_radiology_reports_request ON radiology_reports (radiology_request_id);

-- ── radiology_images ─────────────────────────────────────────────────────────────────
CREATE TABLE radiology_images (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    radiology_request_id BIGINT NOT NULL REFERENCES radiology_requests (id) ON DELETE CASCADE,
    file_path            VARCHAR(255) NOT NULL,
    caption              VARCHAR(255),
    uploaded_by          BIGINT REFERENCES users (id),
    created_at           TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_radiology_images_request ON radiology_images (radiology_request_id);

-- V9: Imagerie médicale (module 9). Imaging orders raised from consultations, the report
-- written by the radiologist, and the images attached. Mirrors the Lab module's
-- request → result shape, but the "result" is a narrative report (findings + conclusion)
-- plus images for the whole request rather than numeric values per item.
--   * radiology_exam_catalog  — seeded reference of exams (radio, écho, scanner, IRM, mammo)
--   * radiology_requests      — the order header (patient, prescriber, priority, status)
--   * radiology_request_items — one line per exam ordered, FK→radiology_exam_catalog
--   * radiology_reports       — the radiologist's compte-rendu, 1:1 with a request
--   * radiology_images        — image files attached to a request (served under /uploads/**)
--
-- DDL kept compatible with BOTH PostgreSQL (prod) and H2 (dev), matching prior migrations.

-- ── radiology_exam_catalog ────────────────────────────────────────────────────
CREATE TABLE radiology_exam_catalog (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code       VARCHAR(30)  NOT NULL UNIQUE,
    name       VARCHAR(150) NOT NULL,
    type       VARCHAR(30),   -- RADIOGRAPHIE, ECHOGRAPHIE, SCANNER, IRM, MAMMOGRAPHIE
    region     VARCHAR(100),
    price      NUMERIC(12,2) NOT NULL,
    is_active  BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_radiology_catalog_is_active ON radiology_exam_catalog (is_active);

